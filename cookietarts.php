<!--        this style tag will give the text bullet points-->
<ul style="list-style-type:circle">
    <center><p>Ingredients</p></center>
    <li>1 pouch (1 lb 1.5 oz) Betty Crocker™ sugar cookie mix</li>
    <li>1/3 cup slivered almonds, toasted, finely choppedr</li>
    <li>1/3 cup butter or margarine, melted</li>
    <li>1/2 teaspoon almond extract/li>
    <li>1 egg</li>
    <li>2/3 cup seedless raspberry jam</li>
    <li>1/3 cup dark or semisweet chocolate chips</li>
</ul>

<!--this type will give each <li> a number-->
<ol type="1">
    <center id="font">Directions</center> 
    <li>Heat oven to 375°F. Line cookie sheet with Reynolds Parchment Paper; set aside. In large bowl, stir together cookie mix and almonds. Stir in melted butter, almond extract and egg until stiff dough forms.</li>
    <li>On floured surface, roll half of dough to 1/4-inch thickness. Cut with 2-inch round or fluted cookie cutter. On lined cookie sheets, place cookies 2 inches apart.</li>
    <li>Bake 7 to 9 minutes minutes or until set. Cool 5 minutes; remove from cookie sheets to cooling racks. Cool completely.</li>
    <li>Meanwhile, on floured surface, roll other half of dough to 1/4-inch thickness. Cut with linzer cutter with hole in center, OR cut with same 2-inch round or fluted cookie cutter and use small 1-inch cutter or bottle top to cut round hole out of center of each cookie. On lined cookie sheets, place cookies 2 inches apart.</li>
    <li>Bake 7 to 9 minutes or until set. Cool 5 minutes; remove from cookie sheets to cooling racks. Cool completely.</li>
    <li>Spread 1 teaspoon jam on bottom of each whole cookie; top each with cutout cookie to make sandwich cookie. In small microwavable bowl, microwave chocolate chips uncovered on High about 1 minute, stirring after 30 seconds, until melted and stirred smooth. Using tip of fork or knife, drizzle chocolate in lines over cookies. Let stand until chocolate is set, about 45 minutes. Store between sheets of Reynolds Parchment Paper in tightly covered container.</li>
</ol>
