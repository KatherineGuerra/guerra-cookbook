        <br> <center id="suggestion">Double-Coconut Cupcake</center>
        <img src="coconutcupcake.jpg" alt="" id="moltenlava"/>
        
        <ul style="list-style-type:circle">
            <center><p>Ingredients</p></center>
            <li>2 cups flaked coconut</li>
            <li>1/2 cup sweetened condensed milk (from 14-oz can)</li>
            <li>1 box Betty Crocker™ SuperMoist™ yellow cake mix</li>
            <li>Water, vegetable oil and eggs called for on cake mix box</li>
             <center><p>Coconut Cream Frosting</p></center> 
             <li>3 cups powdered sugar </li>
             <li>1/3 cup butter or margarine, softened </li>
             <li>1/4 teaspoon salt</li>
             <li>1 teaspoon coconut extract </li>
             <li>1 to 3 tablespoons milk </li>
             <li>1 cup flaked coconut, toasted </li>
                  
        </ul>

        
        <ol type="1">
             <center id="font">Directions</center>
            <li>Heat oven to 350°F (325°F for dark or nonstick pans). Place paper baking cup in each of 24 regular-size muffin cups.</li>
            <li>In medium bowl, stir 2 cups coconut and the condensed milk; set aside.</li>
            <li>Make cake batter as directed on box. Divide batter evenly among muffin cups (two-thirds full). Top each with about 1 heaping teaspoonful coconut mixture.</li>
            <li>Bake as directed on box for 24 cupcakes. Cool in pans 5 minutes; remove from pans to cooling rack. Cool completely, about 30 minutes.</li>
            <li>In medium bowl, beat powdered sugar, butter and salt with spoon or with electric mixer on low speed until well blended. Beat in coconut extract and 1 tablespoon of the milk. Gradually beat in just enough remaining milk to make frosting smooth and spreadable. 
                Immediately frost cupcakes. Dip tops of cupcakes into toasted coconut. Store loosely covered</li>
        </ol>
