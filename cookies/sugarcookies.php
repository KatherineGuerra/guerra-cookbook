    <br> <center id="suggestion">Sugar Cookies</center>
    <img src="sugarcookie.jpg" alt="" id="moltenlava"/>
   
    <ul style="list-style-type:circle">
        <center><p>Ingredients</p></center>
        <li>1 1/2 cups powdered sugar </li>
        <li>1 cup butter or margarine, softened </li>
        <li>1 teaspoon vanilla </li>
        <li>1/2 teaspoon almond extract </li>
        <li>1 egg </li>
        <li>2 1/2 cups Gold Medal™ all-purpose flour</li>
        <li>1 teaspoon baking soda </li>
        <li>1 teaspoon cream of tartar </li>
        <li>Granulated sugar or colored sugar </li>
    </ul>

    <ol type="1">
        <center id="font">Directions</center> 
        <li>Mix powdered sugar, butter, vanilla, almond extract and egg in large bowl. Stir in remaining ingredients except granulated sugar. Cover and refrigerate at least 2 hours.</li>
        <li>Heat oven to 375ºF. Lightly grease cookie sheet.</li>
        <li>Divide dough in half. Roll each half 1/4 inch thick on lightly floured surface. Cut into desired shapes with 2- to 2 1/2-inch cookie cutters. Sprinkle with granulated sugar. Place on cookie sheet.
        </li>
        <li>Bake 7 to 8 minutes or until edges are light brown. Remove from cookie sheet. Cool on wire rack. </li>
    </ol>
