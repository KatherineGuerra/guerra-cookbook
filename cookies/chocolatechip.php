        <br> <p id="suggestion">Chocolate chip cookies</p>
        <img src="chocolatechipcookie.jpg" alt="" id="moltenlava"/>
        <p>ingredients</p>
        <ul style="list-style-type:circle">
            <li>1/2 cup (1 stick) unsalted butter</li>
            <li>3/4 cup packed dark brown sugar</li>
            <li>3/4 cup sugar</li>
            <li>2 large eggs</li>
            <li>1 teaspoon pure vanilla extract</li>
            <li>1 (12-ounce) bag semisweet chocolate chips, or chunks</li>
            <li>2 1/4 cups all-purpose flour</li>
            <li>3/4 teaspoon baking soda</li>
            <li>1 teaspoon fine salt</li>
        </ul>
        <p>Directions</p>
        <ol type="1">
            <li>Evenly position 2 racks in the middle of the oven and preheat to 375 degrees F.
                (on convection setting if you have it.) Line 2 baking sheets with parchment paper or silicone sheets. 
                (If you only have 1 baking sheet, let it cool completely between batches.)
            </li>
            <li>Put the butter in a microwave safe bowl, cover and microwave on medium power until melted. 
                (Alternatively melt in a small saucepan.) Cool slightly. Whisk the sugars, eggs, butter and 
                vanilla in a large bowl until smooth.
            </li>
            <li>Whisk the flour, baking soda and salt in another bowl. Stir the dry ingredients into the wet ingredients with a wooden spoon; take care not to over mix. Stir in the chocolate chips or chunks.
            </li>
            <li>Scoop heaping tablespoons of the dough onto the prepared pans. 
                Wet hands slightly and roll the dough into balls. Space the cookies about 2-inches apart on the pans. 
                Bake, until golden, but still soft in the center, 12 to 16 minutes, depending on how chewy or crunchy you like your cookies. Transfer hot cookies with a spatula to a rack to cool. Serve.
            </li>
        </ol>
