<!--this type will give each <li> a number-->
<ol type="1">
    <center id="font">Directions</center> 
    <li>Heat oven to 350°F (325°F for dark or nonstick pans). Place paper baking cup in each of 24 regular-size muffin cups.</li>
    <li>In large bowl, beat all cupcake ingredients with electric mixer on low speed 30 seconds. Beat on medium speed 2 minutes, scraping bowl occasionally. Divide batter evenly among muffin cups.</li>
    <li>Bake 18 to 20 minutes or until toothpick inserted in center comes out clean. Cool 10 minutes; remove from pans to cooling racks. Cool completely.</li>
    <li>In small bowl, stir lemonade mix and 3 tablespoons water until mix is dissolved. In large bowl, beat butter, whipping cream and lemonade mixture with electric mixer on medium speed 2 minutes or until creamy. On low speed, gradually beat in powdered sugar and food color until blended. Frost cupcakes. Garnish with gummy candies and straws.</li>
</ol>
