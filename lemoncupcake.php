    <center><p>Ingredients</p></center>
    <li>1 box Betty Crocker™ SuperMoist™ lemon cake mix</li>
    <li>3/4 cup water</li>
    <li>1/2 cup vegetable oil</li>
    <li>1/4 cup fresh lemon juice</li>
    <li>3 eggs</li>
    <center><p>Frosting</p></center>
    <li>1/4 cup presweetened lemonade flavor drink mix</li>
    <li>3 tablespoons water</li>
    <li>1/2 cup butter, softened</li>
    <li>2 tablespoons whipping cream</li>
    <li>1box (16 oz) powdered sugar</li>
    <li>2 drops yellow food color</li>
