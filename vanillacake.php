        <br> <center id="suggestion">Vanilla Cake</center>
        <img src="vanillacake.jpg" alt="" id="moltenlava"/>
        
        <ul style="list-style-type:circle">
            <center><p>Ingredients</p></center>
            <li>2 sticks unsalted butter at room temperature</li>
            <li>3 cups all-purpose flour</li>
            <li>1 tablespoon baking powder</li>
            <li>1/2 teaspoon salt</li>
            <li>1 1/2 cups sugar</li>
            <li>4 large eggs</li>
            <li>1 tablespoon vanilla extract</li>
            <li>3/4 cup heavy cream</li>
        </ul>
        
        <ol type="1">
            <center id="font">Directions</center> 
            <li>Preheat the oven to 350 degrees F. Butter two 9-inch-round cake pans and line the
            bottoms with parchment paper, butter the parchment and dust the pans with flour, tapping out the excess.</li>
            <li>Whisk 3 cups flour, the baking powder and salt in a bowl until combined. Beat 2 sticks butter
            and the sugar in a large bowl with a mixer on medium high speed until light and fluffy, about
            3 minutes. Reduce the mixer speed to medium, beat in the eggs, one at a time scraping down the bowl
            as needed. Beat in the vanilla.(The mixture may look separated at this point.)</li>
            <li>Mix 1/2 cup water with the cream in a liquid measuring cup or bowl. Beat the flour mixture
            into the butter mixture in 3 batches, alternating with the cream mixture, beginning and ending 
            with flour, until smooth.</li>
            <li>Divide the batter between the prepared pans. 
            Bake until the cakes are lightly golden on top and the centers spring back when pressed, 
            25 to 30 minutes. let it cool 10 minutes, then run a knife around the edge of the pans and turn the cakes out onto the racks to cool completely.
            Remove the parchment. Trim the tops of the cakes with a long serrated knife to make them level, if desired.
            </li>
        </ol>
